package org.example;

public class Main {

    private static int sumar(int a, int b) {
        return a + b;
    }

    /**
     * Función básica que devuelve el resultado de la diferencia entre dos números
     *
     * @param num1 Un número cualquiera
     * @param num2 Otro número cualquiera
     * @return La diferencia de los dos números
     */
    private static double restar(double num1, double num2) {
        return num1 - num2;
    }

    public static void main(String[] args) {
        System.out.println("Hello world!");
    }
}